-- ① 제1정규형
-- ② 제2정규형
-- ③ 제3정규형
-- ④ 없음

-- 정답: 2
-- 각 속성은 원자값(더 이상 분해할 수 없는 값)을 가지고, 
-- 같은 속성끼리 동일한 자료형을 가지며,
-- 같은 인스턴스는 고유하므로 제1정규형을 만족

-- 이때 member_name은 member_id에만 종속되고,
-- book_title과 category는 book_id에만 종속된다.
-- 따라서 이 경우 부분 함수 종속이 존재하므로, 제2정규형이 위반